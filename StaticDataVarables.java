class StaticDataVarables {
    static int i;
    static float j;
    static long l;
    static double d;
    static char c;
    static boolean s;
    static byte b;
    public static void main(String args[]){
        System.out.println(i);
        System.out.println(j);
        System.out.println(l);
        System.out.println(d);
        System.out.println(c);
        System.out.println(s);
        System.out.println(b);
    }   
}
